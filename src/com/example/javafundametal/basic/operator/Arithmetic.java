package com.example.javafundametal.basic.operator;

public class Arithmetic {
    public static void main(String[] args) {

        //Addition Operator
        System.out.println("Addition :");
        int add = 1 + 5;
        System.out.println("1 + 5 = " + add);
        System.out.println();

        //Subtraction Operator
        System.out.println("Subtraction :");
        int subtract = 5 - 3;
        System.out.println("5 - 3 = " + subtract);
        System.out.println();

        //Multiplication Operator
        System.out.println("multiplication");
        int multiply = 5 * 7;
        System.out.println("5 * 7 = " + multiply);
        System.out.println();

        //Division Operator
        System.out.println("division");
        int divide = 6 / 2;
        System.out.println("6 / 2 = " + divide);
        System.out.println();

        //Modulus Operator
        System.out.println("modulus");
        int modulus = 7 % 3;
        System.out.println("7 % 3 = " + modulus);
    }
}
